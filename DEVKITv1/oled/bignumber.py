def displayBitmap(oled, org_x, org_y, width, height, bitmap):
    for row in range (height):
        for bytecol in range (width):
            byte = bitmap[row*width + bytecol]
            for  i in range (8):
                col = bytecol * 8 + i
                x = org_x + col
                y = org_y + row
                map = 2**(7-i)
                if map & byte:
                    oled.pixel(x, y, 1)
                else:
                    oled.pixel(x, y, 0)
